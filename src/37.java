public class MathProject {
    public static void main(String[] args) {
        // Example code to calculate simple addition and subtraction
        int num1 = 5;
        int num2 = 3;
        int sum = num1 + num2;
        int difference = num1 - num2;

        System.out.println("The result of " + num1 + " + " + num2 + " is: " + sum);
        System.out.println("The result of " + num1 + " - " + num2 + " is: " + difference);
    }
}
